public class Persons {
	private String name;
	private int age;

	public Persons(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	// Getters
	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	// Setters
	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}
}
